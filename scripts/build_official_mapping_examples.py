def _write_batch_query(
    path: Path,
    sample_rows: dict[str, dict[str, float]],
    band_ids: tuple[str, ...],
    *,
    exclude_row_ids: dict[str, str] | None = None,
) -> None:
    include_exclude_row_id = bool(exclude_row_ids)
    fieldnames = ["sample_id"]
    if include_exclude_row_id:
        fieldnames.append("exclude_row_id")
    fieldnames.extend(band_ids)
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=fieldnames, lineterminator="\n")
        writer.writeheader()
        for sample_id, values in sample_rows.items():
            row = {"sample_id": sample_id}
            if include_exclude_row_id:
                row["exclude_row_id"] = (exclude_row_ids or {}).get(sample_id, "")
            row.update({band_id: round(float(values[band_id]), 8) for band_id in band_ids})
            writer.writerow(row)


