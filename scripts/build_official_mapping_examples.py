def _reset_generated_outputs() -> None:
    for root in (SRF_ROOT, QUERIES_ROOT, RESULTS_ROOT, EXAMPLES_ROOT / "siac"):
        if root.exists():
            shutil.rmtree(root)
        if root != EXAMPLES_ROOT / "siac":
            root.mkdir(parents=True, exist_ok=True)
    DOCS_ASSETS_ROOT.mkdir(parents=True, exist_ok=True)
    for path in DOCS_ASSETS_ROOT.glob("official_*.png"):
        path.unlink()


