def _write_pairwise_metrics(
    prepared_root: Path,
    truth_rows_by_sensor: dict[str, dict[str, dict[str, float]]],
) -> dict[tuple[str, str], dict[str, float]]:
    metrics_root = RESULTS_ROOT / "metrics"
    metrics_root.mkdir(parents=True, exist_ok=True)
    rows: list[dict[str, object]] = []
    metrics: dict[tuple[str, str], dict[str, float]] = {}

    mapper = SpectralMapper(prepared_root)

    for source_sensor in SENSOR_BY_ID:
        for target_sensor in SENSOR_BY_ID:
            if source_sensor == target_sensor:
                continue
            sample_mae: list[float] = []
            sample_rmse: list[float] = []
            for sample_id, reflectance in truth_rows_by_sensor[source_sensor].items():
                target = HELD_OUT_TARGETS_BY_ID[sample_id]
                mapping_result = mapper._map_reflectance_internal(
                    source_sensor=source_sensor,
                    reflectance=reflectance,
                    valid_mask=None,
                    output_mode="target_sensor",
                    target_sensor=target_sensor,
                    k=EXAMPLE_K,
                    min_valid_bands=1,
                    candidate_row_indices=mapper.candidate_row_indices(exclude_row_ids=[target.row_id]),
                )
                if mapping_result.target_reflectance is None:
                    raise RuntimeError(
                        f"Missing target_reflectance for {source_sensor} -> {target_sensor} sample {sample_id}."
                    )
                mapped = {
                    band_id: float(value)
                    for band_id, value in zip(mapping_result.target_band_ids, mapping_result.target_reflectance)
                }
                truth = truth_rows_by_sensor[target_sensor][sample_id]
                sample_mae.append(_mean_abs_error(mapped, truth, COMPARABLE_PAIRWISE_BANDS))
                sample_rmse.append(_rmse(mapped, truth, COMPARABLE_PAIRWISE_BANDS))

            payload = {
                "mean_abs_error": float(np.mean(sample_mae)),
                "rmse": float(np.mean(sample_rmse)),
                "sample_count": len(sample_mae),
                "evaluated_band_ids": list(COMPARABLE_PAIRWISE_BANDS),
                "evaluated_band_count": len(COMPARABLE_PAIRWISE_BANDS),
            }
            metrics[(source_sensor, target_sensor)] = payload
            rows.append(
                {
                    "source_sensor": source_sensor,
                    "target_sensor": target_sensor,
                    "source_label": SENSOR_BY_ID[source_sensor].short_label,
                    "target_label": SENSOR_BY_ID[target_sensor].short_label,
                    "evaluated_band_ids": "|".join(COMPARABLE_PAIRWISE_BANDS),
                    "evaluated_band_count": len(COMPARABLE_PAIRWISE_BANDS),
                    "mean_abs_error": payload["mean_abs_error"],
                    "rmse": payload["rmse"],
                    "sample_count": payload["sample_count"],
                }
            )

    csv_path = metrics_root / "pairwise_band_metrics.csv"
    with csv_path.open("w", encoding="utf-8", newline="") as handle:
        writer = csv.DictWriter(
            handle,
            fieldnames=[
                "source_sensor",
                "target_sensor",
                "source_label",
                "target_label",
                "evaluated_band_ids",
                "evaluated_band_count",
                "mean_abs_error",
                "rmse",
                "sample_count",
            ],
            lineterminator="\n",
        )
        writer.writeheader()
        writer.writerows(rows)

    json_rows = [
        {
            **row,
            "evaluated_band_ids": list(COMPARABLE_PAIRWISE_BANDS),
        }
        for row in rows
    ]
    _json_write(
        metrics_root / "pairwise_band_metrics.json",
        {
            "comparison_band_ids": list(COMPARABLE_PAIRWISE_BANDS),
            "pairs": json_rows,
        },
    )
    return metrics


