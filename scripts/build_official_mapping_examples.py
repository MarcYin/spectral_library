def _mean_abs_error(mapped: dict[str, float], truth: dict[str, float], band_ids: tuple[str, ...]) -> float:
    residuals = _metric_values(mapped, truth, band_ids)
    return float(np.mean(np.abs(residuals)))


def _rmse(mapped: dict[str, float], truth: dict[str, float], band_ids: tuple[str, ...]) -> float:
    residuals = _metric_values(mapped, truth, band_ids)
    return float(np.sqrt(np.mean(residuals**2)))


