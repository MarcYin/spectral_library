def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description="Regenerate the official MODIS, Sentinel-2A, and Landsat mapping example bundle.",
    )
    parser.add_argument(
        "--siac-root",
        default="",
        help=(
            "Path to the previously composed full SIAC library root. "
            f"Defaults to {FULL_LIBRARY_ENV_VAR}, then {DEFAULT_FULL_LIBRARY_ROOT.relative_to(REPO_ROOT)} if present."
        ),
    )
    return parser


def main(argv: list[str] | None = None) -> int:
    args = build_parser().parse_args(argv)
    build_official_mapping_examples(siac_root=Path(args.siac_root) if args.siac_root else None)
    print(json.dumps({"status": "ok", "examples_root": str(EXAMPLES_ROOT)}, indent=2))
    return 0


