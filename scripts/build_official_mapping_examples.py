def _resolve_full_library_root(explicit_root: Path | None = None) -> Path:
    candidates: list[Path] = []
    env_value = os.environ.get(FULL_LIBRARY_ENV_VAR, "").strip()
    if explicit_root is not None:
        candidates.append(explicit_root)
    elif env_value:
        candidates.append(Path(env_value))
    if DEFAULT_FULL_LIBRARY_ROOT not in candidates:
        candidates.append(DEFAULT_FULL_LIBRARY_ROOT)
    discovered = sorted(
        (path for path in (REPO_ROOT / "build").glob("siac_spectral_library_*") if path.is_dir()),
        key=lambda path: path.stat().st_mtime,
        reverse=True,
    )
    candidates.extend(path for path in discovered if path not in candidates)

    for candidate in candidates:
        metadata_path = candidate / "tabular" / "siac_spectra_metadata.csv"
        spectra_path = candidate / "tabular" / "siac_normalized_spectra.csv"
        if metadata_path.exists() and spectra_path.exists():
            return candidate

    raise SystemExit(
        "Official example regeneration requires a composed full SIAC library. "
        f"Set {FULL_LIBRARY_ENV_VAR} or pass --siac-root to a build directory that contains "
        "tabular/siac_spectra_metadata.csv and tabular/siac_normalized_spectra.csv."
    )


def _inspect_full_library(
    siac_root: Path,
) -> tuple[int, dict[str, int], dict[str, dict[str, str]]]:
    metadata_path = siac_root / "tabular" / "siac_spectra_metadata.csv"
    targets_by_key = {
        (target.source_id, target.spectrum_id, target.sample_name): target
        for target in HELD_OUT_TARGETS
    }
    row_count = 0
    landcover_counts: Counter[str] = Counter()
    heldout_metadata: dict[str, dict[str, str]] = {}
    with metadata_path.open("r", encoding="utf-8", newline="") as handle:
        reader = csv.DictReader(handle)
        for row in reader:
            row_count += 1
            landcover_counts[row.get("landcover_group") or "unlabeled"] += 1
            key = (row.get("source_id") or "", row.get("spectrum_id") or "", row.get("sample_name") or "")
            target = targets_by_key.get(key)
            if target is not None:
                heldout_metadata[target.sample_id] = dict(row)
    missing = sorted(target.sample_id for target in HELD_OUT_TARGETS if target.sample_id not in heldout_metadata)
    if missing:
        raise RuntimeError(f"Full SIAC library is missing configured held-out targets: {missing}")
    return row_count, dict(sorted(landcover_counts.items())), heldout_metadata


def _load_selected_spectra(
    siac_root: Path,
    row_keys_by_output_id: dict[str, tuple[str, str, str]],
) -> dict[str, np.ndarray]:
    spectra_path = siac_root / "tabular" / "siac_normalized_spectra.csv"
    wanted = dict(row_keys_by_output_id)
    nm_columns = [f"nm_{int(wavelength)}" for wavelength in CANONICAL_WAVELENGTHS]
    found: dict[str, np.ndarray] = {}
    with spectra_path.open("r", encoding="utf-8", newline="") as handle:
        reader = csv.DictReader(handle)
        for row in reader:
            key = (row.get("source_id") or "", row.get("spectrum_id") or "", row.get("sample_name") or "")
            for output_id, expected in wanted.items():
                if key != expected:
                    continue
                raw_values = np.asarray(
                    [float(row[column]) if row[column] not in {"", None} else np.nan for column in nm_columns],
                    dtype=np.float64,
                )
                if np.isnan(raw_values).any():
                    valid = np.isfinite(raw_values)
                    if valid.sum() < 2:
                        raise RuntimeError(f"Insufficient valid points while loading {output_id}.")
                    spectrum = np.interp(CANONICAL_WAVELENGTHS, CANONICAL_WAVELENGTHS[valid], raw_values[valid])
                else:
                    spectrum = raw_values
                if spectrum.shape != CANONICAL_WAVELENGTHS.shape:
                    raise RuntimeError(f"Spectrum width mismatch while loading {output_id}.")
                found[output_id] = spectrum
                break
            if len(found) == len(wanted):
                break
    missing = sorted(output_id for output_id in wanted if output_id not in found)
    if missing:
        raise RuntimeError(f"Missing spectra for requested ids: {missing}")
    return found


def _load_neighbor_spectra(siac_root: Path, row_ids: set[str]) -> dict[str, np.ndarray]:
    row_keys_by_output_id: dict[str, tuple[str, str, str]] = {}
    for row_id in row_ids:
        source_id, spectrum_id, sample_name = row_id.split(":", 2)
        row_keys_by_output_id[row_id] = (source_id, spectrum_id, sample_name)
    return _load_selected_spectra(siac_root, row_keys_by_output_id)


def _sha256_bytes(payload: bytes) -> str:
    return hashlib.sha256(payload).hexdigest()


def _download(url: str, destination: Path) -> DownloadedArtifact:
    destination.parent.mkdir(parents=True, exist_ok=True)
    downloaded_at_utc = datetime.now(timezone.utc).isoformat()
    with urlopen(url, timeout=60) as response:
        payload = response.read()
        etag = response.headers.get("ETag")
        last_modified = response.headers.get("Last-Modified")
    destination.write_bytes(payload)
    return DownloadedArtifact(
        url=url,
        sha256=_sha256_bytes(payload),
        size_bytes=len(payload),
        downloaded_at_utc=downloaded_at_utc,
        local_name=destination.name,
        etag=etag,
        last_modified=last_modified,
    )


def _download_json(url: str) -> tuple[list[dict[str, object]], DownloadedArtifact]:
    downloaded_at_utc = datetime.now(timezone.utc).isoformat()
    with urlopen(url, timeout=60) as response:
        payload_bytes = response.read()
        etag = response.headers.get("ETag")
        last_modified = response.headers.get("Last-Modified")
    payload = json.loads(payload_bytes.decode("utf-8"))
    artifact = DownloadedArtifact(
        url=url,
        sha256=_sha256_bytes(payload_bytes),
        size_bytes=len(payload_bytes),
        downloaded_at_utc=downloaded_at_utc,
        local_name=Path(url).name,
        etag=etag,
        last_modified=last_modified,
    )
    return payload, artifact


def _json_write(path: Path, payload: object) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(payload, indent=2, sort_keys=False) + "\n", encoding="utf-8")


def _weighted_center(wavelength_nm: np.ndarray, rsr: np.ndarray) -> float:
    return float(np.dot(wavelength_nm, rsr) / rsr.sum())


def _fwhm_nm(wavelength_nm: np.ndarray, rsr: np.ndarray) -> float:
    mask = rsr >= 0.5
    if not np.any(mask):
        return float(wavelength_nm[-1] - wavelength_nm[0])
    return float(wavelength_nm[mask][-1] - wavelength_nm[mask][0])


def _band_payload(band_id: str, segment: str, wavelengths_nm: np.ndarray, rsr: np.ndarray) -> dict[str, object]:
    support = rsr > 0
    return {
        "band_id": band_id,
        "segment": segment,
        "wavelength_nm": [round(float(value), 4) for value in wavelengths_nm.tolist()],
        "rsr": [round(float(value), 8) for value in rsr.tolist()],
        "center_nm": round(_weighted_center(wavelengths_nm, rsr), 4),
        "fwhm_nm": round(_fwhm_nm(wavelengths_nm, rsr), 4),
        "support_min_nm": round(float(wavelengths_nm[support][0]), 4),
        "support_max_nm": round(float(wavelengths_nm[support][-1]), 4),
    }


def _parse_modis_sensor(selection: SensorSelection, workbook_path: Path) -> dict[str, object]:
    workbook = load_workbook(workbook_path, read_only=True, data_only=True)
    bands: list[dict[str, object]] = []
    for band in selection.bands:
        worksheet = workbook[band.source_key]
        samples_by_wavelength: dict[float, list[float]] = {}
        for row in worksheet.iter_rows(min_row=2, values_only=True):
            if not row or row[2] is None or row[3] is None:
                continue
            wavelength = float(row[2])
            value = float(row[3])
            if value <= 0:
                continue
            samples_by_wavelength.setdefault(wavelength, []).append(value)
        wavelengths_nm = np.asarray(sorted(samples_by_wavelength), dtype=np.float64)
        rsr = np.asarray(
            [float(np.mean(samples_by_wavelength[wavelength])) for wavelength in wavelengths_nm],
            dtype=np.float64,
        )
        bands.append(_band_payload(band.band_id, band.segment, wavelengths_nm, rsr))
    return {"sensor_id": selection.sensor_id, "bands": bands}


def _parse_sentinel_sensor(selection: SensorSelection, workbook_path: Path) -> dict[str, object]:
    workbook = load_workbook(workbook_path, read_only=True, data_only=True)
    worksheet = workbook["Spectral Responses (S2A)"]
    rows = list(worksheet.iter_rows(values_only=True))
    header = [value if value is not None else "" for value in rows[0]]
    columns = {str(name): index for index, name in enumerate(header)}
    wavelength_index = columns["SR_WL"]
    bands: list[dict[str, object]] = []
    for band in selection.bands:
        response_index = columns[band.source_key]
        wavelengths: list[float] = []
        response: list[float] = []
        for row in rows[1:]:
            if row[wavelength_index] is None or row[response_index] is None:
                continue
            rsr = float(row[response_index])
            if rsr <= 0:
                continue
            wavelengths.append(float(row[wavelength_index]))
            response.append(rsr)
        bands.append(
            _band_payload(
                band.band_id,
                band.segment,
                np.asarray(wavelengths, dtype=np.float64),
                np.asarray(response, dtype=np.float64),
            )
        )
    return {"sensor_id": selection.sensor_id, "bands": bands}


def _parse_landsat_sensor(selection: SensorSelection) -> tuple[dict[str, object], list[dict[str, object]]]:
    base_url = "https://landsat.usgs.gov/landsat/spectral_viewer/c3-master/htdocs/data/bands"
    bands: list[dict[str, object]] = []
    artifacts: list[dict[str, object]] = []
    for band in selection.bands:
        payload, artifact = _download_json(f"{base_url}/{band.source_key}")
        wavelength_key, response_key = list(payload[0])
        wavelengths_nm = np.asarray([1000.0 * float(item[wavelength_key]) for item in payload], dtype=np.float64)
        rsr = np.asarray([float(item[response_key]) for item in payload], dtype=np.float64)
        positive = rsr > 0
        bands.append(_band_payload(band.band_id, band.segment, wavelengths_nm[positive], rsr[positive]))
        artifact_payload = artifact.to_dict()
        artifact_payload["official_band"] = band.official_band
        artifact_payload["band_id"] = band.band_id
        artifacts.append(artifact_payload)
    return {"sensor_id": selection.sensor_id, "bands": bands}, artifacts


def _simulate_sensor(sensor_schema: dict[str, object], spectrum: np.ndarray) -> dict[str, float]:
    values: dict[str, float] = {}
    for band in sensor_schema["bands"]:  # type: ignore[index]
        wavelengths = np.asarray(band["wavelength_nm"], dtype=np.float64)  # type: ignore[index]
        response = np.asarray(band["rsr"], dtype=np.float64)  # type: ignore[index]
        sampled = np.interp(wavelengths, CANONICAL_WAVELENGTHS, spectrum)
        values[str(band["band_id"])] = float(np.dot(sampled, response) / response.sum())
    return values


