def _run_cli(*args: str) -> str:
    command = [sys.executable, "-m", "spectral_library.cli", *args]
    completed = subprocess.run(
        command,
        check=False,
        cwd=REPO_ROOT,
        env=_python_env(),
        text=True,
        capture_output=True,
    )
    if completed.returncode != 0:
        if completed.stdout:
            print(completed.stdout, file=sys.stdout)
        if completed.stderr:
            print(completed.stderr, file=sys.stderr)
        raise subprocess.CalledProcessError(completed.returncode, command, output=completed.stdout, stderr=completed.stderr)
    return completed.stdout


def _metric_values(mapped: dict[str, float], truth: dict[str, float], band_ids: tuple[str, ...]) -> np.ndarray:
    missing = [band_id for band_id in band_ids if band_id not in mapped or band_id not in truth]
    if missing:
        raise RuntimeError(f"Missing metric bands {missing} while computing pairwise example metrics.")
    return np.asarray([mapped[band_id] - truth[band_id] for band_id in band_ids], dtype=np.float64)


